/**
 * DownloadsController
 */
class DownloadsController extends \TYPO3\CMS\Extbase\Mvc\Controller\ActionController
{
    /**
     * action list
     *
     * @return void
     */
    public function listAction()
    {
        $downloads = $this->downloadsRepository->findAll();
        $this->view->assign('downloads', $downloads);
    }
}
